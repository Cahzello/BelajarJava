public class ForEach {
    public static void main(String[] args) {
        String[] names = {
            "Kasumi Toyama", "Arisa Ichigaya", "Hanazono Tae", "Rimi Ushigome",
                "Saaya Yamabuki"
        };

        for (var value : names){
            System.out.println(value);
        }


    }
}
